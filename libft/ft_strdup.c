#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;
	int		i;
	int		len;

	dest = (char *)malloc(ft_strlen(str) + 1);
	len = ft_strlen(str);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
